package griffio.planets;

import com.google.common.collect.ComparisonChain;

import java.util.Comparator;
import java.util.Objects;

public final class Planets implements Comparator<Planet> {
    @Override
    public int compare(Planet a, Planet b) {
        return ComparisonChain.start().compare(a.au(), b.au()).result();
    }

    public static int hash(String name, double au) {
        return Objects.hash(name, au);
    }

    public static boolean isEqual(Planet a, Planet b) {
        return Objects.equals(a.names(), b.names()) && Objects.equals(a.au(), b.au());
    }
}
